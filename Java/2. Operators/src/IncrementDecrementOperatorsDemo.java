public class IncrementDecrementOperatorsDemo {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 10;

        System.out.println("Initial value: " + num1);
        System.out.println("Value after increment: " + ++num1);
        System.out.println("Value after decrement: " + --num2);
    }


}
